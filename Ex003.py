# Exercício 003: Escrever um algorítmo que some dois números informados pelo usuário.

# Solicitando os dois valores numéricos para salvar nas respectivas variáveis e somando ambos.
num1 = int(input('Informe o primeiro número para a soma: '))
num2 = int(input('Informe o segundo número para a soma: '))
soma = num1 + num2
print('O resultado da soma entre {} e {} é: {}'.format(num1, num2, soma))

